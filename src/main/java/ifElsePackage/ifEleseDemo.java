package ifElsePackage;

public class ifEleseDemo {

	public void ifElseLogic(String day) {
		
		if(day.equals("Monday")) {
			System.out.println("Its "+day+"");
		}else if(day.equals("Tuesday")) {
			System.out.println("Its "+day+"");
		}else if(day.equals("Saturday")) {
			System.out.println("Its "+day+"");
		}else {
			System.out.println("doesnt match any day");
		}

	}
}
